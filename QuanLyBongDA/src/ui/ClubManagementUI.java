    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        PlayerBLL.addPlayerNameInList(jList1, ((Club) jComboBox1.getSelectedItem()).getClubID());
        Club club = (Club) jComboBox1.getSelectedItem();
        txtID.setText(club.getClubID());
        txtName.setText(club.getClubName());
        txtAddress.setText(club.getAddress());
        txtWebsite.setText(club.getWebsite());
        String stadiumID = club.getStadiumID();
        for (int i = 0; i < jComboBox2.getItemCount(); i++) {
            if (jComboBox2.getItemAt(i).getStadiumID().equals(stadiumID)) {
                jComboBox2.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtName.getText().equals("") || txtID.getText().equals("") || txtAddress.getText().equals("") || txtWebsite.getText().equals("") || jComboBox1.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Nhập chưa đủ thông tin", "Hãy nhập đủ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(CheckingInputData.checkDuplicateID(jComboBox1, txtID.getText())){
            JOptionPane.showMessageDialog(null, "Trùng ID");
            return;
        }
        Club club = new Club(txtID.getText(), txtName.getText(), txtAddress.getText(), txtWebsite.getText(), ((Stadium) jComboBox2.getSelectedItem()).getStadiumID());
        ClubBLL.addClub(club);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Phải chọn CLB muốn sửa", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Club club = new Club(txtID.getText(), txtName.getText(), txtAddress.getText(), txtWebsite.getText(), ((Stadium) jComboBox2.getSelectedItem()).getStadiumID());
        ClubBLL.updateClub(club);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Phải chọn CLB muốn xóa", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Club club = new Club(txtID.getText(), txtName.getText(), txtAddress.getText(), txtWebsite.getText(), ((Stadium) jComboBox2.getSelectedItem()).getStadiumID());
        ClubBLL.deleteClub(club);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton4ActionPerformed


