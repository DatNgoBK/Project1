    public void updateData() {
        try {
            Thread.sleep(MainFrameUI.DELAY);
            ClubBLL.showClubInformation(jComboBox1);
            StadiumBLL.showStadiumInformationCbo(jComboBox2);
            PlayerBLL.addPlayerNameInList(jList1, ((Club) jComboBox1.getSelectedItem()).getClubID());
        } catch (InterruptedException ex) {
            Logger.getLogger(ClubManagementUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

