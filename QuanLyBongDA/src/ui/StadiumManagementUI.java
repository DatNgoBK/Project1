    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Phải chọn một SVĐ", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        if(CheckingInputData.checkDuplicateID(jTable1, txtID.getText())){
            JOptionPane.showMessageDialog(null, "Trùng ID");
            return;
        }
        Stadium stadium = new Stadium(txtID.getText(), txtName.getText(), txtYear.getText(), Integer.valueOf(txtCapacity.getText()), txtNote.getText());
        StadiumBLL.addStadium(stadium);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Phải chọn một SVĐ", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Stadium stadium = new Stadium(txtID.getText(), txtName.getText(), txtYear.getText(), Integer.valueOf(txtCapacity.getText()), txtNote.getText());
        StadiumBLL.updateStadium(stadium);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!checkInput()) {
            return;
        }
        Stadium stadium = new Stadium(txtID.getText(), txtName.getText(), txtYear.getText(), Integer.valueOf(txtCapacity.getText()), txtNote.getText());
        StadiumBLL.deleteStadium(stadium);
        updateData();
        resetForm();
    }//GEN-LAST:event_jButton3ActionPerformed

