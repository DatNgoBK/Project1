    public void updateData() {
        try {
            Thread.sleep(MainFrameUI.DELAY);
            StadiumBLL.showStadiumInformation(jTable1);
        } catch (InterruptedException ex) {
            Logger.getLogger(StadiumManagementUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Kiểm tra dữ liệu vào
     *
     * @return Kết quả kiểm tra
     */
    private boolean checkInput() {
        if (txtName.getText().equals("") || txtID.getText().equals("") || txtYear.getText().equals("") || txtNote.getText().equals("") || txtCapacity.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập chưa đủ thông tin", "Hãy nhập đủ", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!CheckingInputData.checkName(txtName.getText()) || !CheckingInputData.checkSpace(txtName.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtName.setText("");
            txtName.requestFocus();
            return false;
        }
        return true;
    }

    /**
     * Làm trắng các TextField điền thông tin
     */
    private void resetForm() {
        txtID.setText("");
        txtName.setText("");
        txtYear.setText("");
        txtNote.setText("");
        txtCapacity.setText("");
    }
